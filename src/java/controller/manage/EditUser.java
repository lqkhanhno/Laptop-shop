    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id");
        User u = new UserDAO().getUserByID(id);
        request.setAttribute("user", u);
        Vector<Role> listRole = new RoleDAO().listAllRole();
        request.setAttribute("listRole", listRole);
        request.getRequestDispatcher("/manager/edituser.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id");
        String fullname = (String) request.getParameter("fullname");
        String username = (String) request.getParameter("username");
        String password = (String) request.getParameter("password");
        String email = (String) request.getParameter("email");
        String roleID =  request.getParameter("roleID");
        String userAddress = (String) request.getParameter("address");
        String phoneNumber = (String) request.getParameter("phone");
        
        User u = new User(Integer.parseInt(id), fullname, username, password, email, Integer.parseInt(roleID), userAddress, phoneNumber);
        new UserDAO().updateUserInManage(u);
        response.sendRedirect("usermanage");
    }


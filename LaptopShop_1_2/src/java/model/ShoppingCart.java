package model;


public class ShoppingCart {
    int ID;
    int userID;

    public ShoppingCart() {
    }

    public ShoppingCart(int ID, int userID) {
        this.ID = ID;
        this.userID = userID;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

   

    
}
